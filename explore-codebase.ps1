    Write-Output ""

    $dataAccessFiles = @()
    Get-ChildItem -Path $Root -Filter "*.cs" -Recurse -File |
        Where-Object { -not (Excluded $_.FullName) } |
        ForEach-Object {
            $content = Get-Content $_.FullName -Raw
            $signals = @()
            if ($content -match 'SqlConnection|NpgsqlConnection|MySqlConnection|SqliteConnection|OracleConnection') { $signals += "DbConnection" }
            if ($content -match 'IDbConnection|IDbCommand') { $signals += "IDbConnection" }
            if ($content -match '\.QueryAsync|\.ExecuteAsync|\.QueryFirstAsync|\.QuerySingleAsync') { $signals += "Dapper" }
            if ($content -match 'SELECT\s|INSERT\s|UPDATE\s|DELETE\s|CREATE\s|ALTER\s') { $signals += "RawSQL" }
            if ($content -match 'SqlCommand|SqlDataReader|SqlDataAdapter') { $signals += "ADO.NET" }
            if ($signals.Count -gt 0) {
                $dataAccessFiles += [PSCustomObject]@{ File = Rel $_.FullName; Patterns = $signals -join ', ' }
            }
        }

    if ($dataAccessFiles.Count -gt 0) {
        Write-Output "| File | Patterns |"
        Write-Output "|---|---|"
        foreach ($f in $dataAccessFiles) { Write-Output "| ``$($f.File)`` | $($f.Patterns) |" }
        Write-Output ""
    } else {
        Write-Output "_No data access patterns detected in source files._"
        Write-Output ""
    }
}

