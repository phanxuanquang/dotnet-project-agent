Write-Output "## 9. Code Convention Signals"
Write-Output ""

$sampleFiles = Get-ChildItem -Path $Root -Filter "*.cs" -Recurse -File |
    Where-Object { -not (Excluded $_.FullName) -and $_.FullName -notmatch '\\(Migrations|AssemblyInfo)\\' } |
    Select-Object -First 10

$signals = [ordered]@{
    'file-scoped-namespace'  = 0
    'block-scoped-namespace' = 0
    'nullable-enable'        = 0
    'record-types'           = 0
    'primary-constructors'   = 0
    'collection-expressions' = 0
    'global-using'           = 0
    'async-await'            = 0
    'init-properties'        = 0
}

foreach ($f in $sampleFiles) {
    $content = Get-Content $f.FullName -Raw
    if ($content -match 'namespace\s+\S+;')           { $signals['file-scoped-namespace']++ }
    if ($content -match 'namespace\s+\S+\s*\{')       { $signals['block-scoped-namespace']++ }
    if ($content -match '#nullable\s+enable|<Nullable>enable') { $signals['nullable-enable']++ }
