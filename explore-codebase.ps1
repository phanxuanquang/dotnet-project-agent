<#
.SYNOPSIS
    Codebase exploration script for bootstrapping GitHub Copilot agents.
    Outputs a Markdown-formatted report of project structure, dependencies,
    database providers, frontend signals, and code conventions.

.DESCRIPTION
    Run from the repository root. Produces a structured Markdown report to stdout
    that both AI agents and humans can read without further processing.

.EXAMPLE
    cd C:\path\to\repo
    .\eng\explore-codebase.ps1
    .\eng\explore-codebase.ps1 | Out-File -FilePath exploration-report.md -Encoding utf8
#>

param(
    [string]$Root = (Get-Location).Path
)

$ErrorActionPreference = 'SilentlyContinue'

# ─────────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────────
function Rel([string]$FullPath) { $FullPath.Replace($Root, '.').Replace('\', '/') }
function Excluded { param([string]$Path) $Path -match '[\\/](bin|obj|node_modules|\.git|\.vs|packages|TestResults|\.idea)[\\/]' }

Write-Output "# Codebase Exploration Report"
Write-Output ""
Write-Output "> Auto-generated by ``explore-codebase.ps1`` on $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss')"
Write-Output "> Root: ``$Root``"
Write-Output ""

# ═════════════════════════════════════════════
# 1. Solution & Project Structure
# ═════════════════════════════════════════════
Write-Output "## 1. Solution & Project Files"
Write-Output ""

$slnFiles = Get-ChildItem -Path $Root -Filter "*.sln" -Recurse -File | Where-Object { -not (Excluded $_.FullName) }
if ($slnFiles) {
    Write-Output "### Solution Files"
    Write-Output ""
    foreach ($sln in $slnFiles) { Write-Output "- ``$(Rel $sln.FullName)``" }
    Write-Output ""
}

$csprojFiles = Get-ChildItem -Path $Root -Filter "*.csproj" -Recurse -File | Where-Object { -not (Excluded $_.FullName) }

if ($csprojFiles) {
    Write-Output "### Projects"
    Write-Output ""

    foreach ($csproj in $csprojFiles) {
        $relativePath = Rel $csproj.FullName
        [xml]$xml = Get-Content $csproj.FullName -Raw

        $tf = $xml.Project.PropertyGroup.TargetFramework | Where-Object { $_ } | Select-Object -First 1
        $tfs = $xml.Project.PropertyGroup.TargetFrameworks | Where-Object { $_ } | Select-Object -First 1
        $framework = if ($tf) { $tf } elseif ($tfs) { $tfs } else { "?" }
        $nullable = $xml.Project.PropertyGroup.Nullable | Where-Object { $_ } | Select-Object -First 1
        $outputType = $xml.Project.PropertyGroup.OutputType | Where-Object { $_ } | Select-Object -First 1

        Write-Output "#### ``$relativePath``"
        Write-Output ""

        $meta = @("**Framework**: ``$framework``")
        if ($outputType) { $meta += "**Output**: ``$outputType``" }
        if ($nullable)   { $meta += "**Nullable**: ``$nullable``" }
        Write-Output ($meta -join ' | ')
        Write-Output ""

        # Package references
        $pkgRefs = $xml.Project.ItemGroup.PackageReference | Where-Object { $_.Include }
        if ($pkgRefs) {
            Write-Output "| Package | Version |"
            Write-Output "|---|---|"
            foreach ($pkg in $pkgRefs) {
                $ver = if ($pkg.Version) { $pkg.Version } else { "—" }
                Write-Output "| ``$($pkg.Include)`` | $ver |"
            }
            Write-Output ""
        }

        # Project references
        $projRefs = $xml.Project.ItemGroup.ProjectReference | Where-Object { $_.Include }
        if ($projRefs) {
            Write-Output "**Project References**:"
            foreach ($ref in $projRefs) { Write-Output "- ``$($ref.Include)``" }
            Write-Output ""
        }
    }
}

# ═════════════════════════════════════════════
# 2. Database Detection
# ═════════════════════════════════════════════
Write-Output "## 2. Database & Data Access"
Write-Output ""

$dbPackagePatterns = @{
    'SQL Server'       = 'Microsoft\.EntityFrameworkCore\.SqlServer|System\.Data\.SqlClient|Microsoft\.Data\.SqlClient'
    'Azure SQL'        = 'Microsoft\.EntityFrameworkCore\.SqlServer|Microsoft\.Azure\.Services\.AppAuthentication'
    'PostgreSQL'       = 'Npgsql|Npgsql\.EntityFrameworkCore\.PostgreSQL'
    'MySQL'            = 'MySql\.EntityFrameworkCore|Pomelo\.EntityFrameworkCore\.MySql|MySql\.Data|MySqlConnector'
    'MariaDB'          = 'Pomelo\.EntityFrameworkCore\.MySql|MySqlConnector'
    'SQLite'           = 'Microsoft\.EntityFrameworkCore\.Sqlite|System\.Data\.SQLite|Microsoft\.Data\.Sqlite'
    'Oracle'           = 'Oracle\.EntityFrameworkCore|Oracle\.ManagedDataAccess'
    'Snowflake'        = 'Snowflake\.Data'
}

$ormPatterns = @{
    'Entity Framework Core' = 'Microsoft\.EntityFrameworkCore'
    'Dapper'                = '^Dapper$|^Dapper\.'
    'ADO.NET (SqlClient)'   = 'System\.Data\.SqlClient|Microsoft\.Data\.SqlClient'
    'ADO.NET (Npgsql)'      = '^Npgsql$'
    'ADO.NET (MySql)'       = '^MySql\.Data$|^MySqlConnector$'
    'ADO.NET (SQLite)'      = 'System\.Data\.SQLite|Microsoft\.Data\.Sqlite'
    'ADO.NET (Oracle)'      = 'Oracle\.ManagedDataAccess'
    'RepoDB'                = '^RepoDb'
    'Linq2db'               = '^linq2db'
}

$allPackages = @()
foreach ($csproj in $csprojFiles) {
    [xml]$xml = Get-Content $csproj.FullName -Raw
    $pkgRefs = $xml.Project.ItemGroup.PackageReference | Where-Object { $_.Include }
    foreach ($pkg in $pkgRefs) { $allPackages += $pkg.Include }
}

# Database providers
$detectedDb = @()
foreach ($db in $dbPackagePatterns.Keys) {
    $pattern = $dbPackagePatterns[$db]
    if ($allPackages | Where-Object { $_ -match $pattern }) { $detectedDb += $db }
}

# ORM / data access
$detectedOrm = @()
foreach ($orm in $ormPatterns.Keys) {
    $pattern = $ormPatterns[$orm]
    if ($allPackages | Where-Object { $_ -match $pattern }) { $detectedOrm += $orm }
}

Write-Output "| Category | Detected |"
Write-Output "|---|---|"
Write-Output "| **Database Engine(s)** | $(if ($detectedDb.Count -gt 0) { $detectedDb -join ', ' } else { '_None detected_' }) |"
Write-Output "| **ORM / Data Access** | $(if ($detectedOrm.Count -gt 0) { $detectedOrm -join ', ' } else { '_None detected_' }) |"
Write-Output ""

$hasEfCore = $allPackages | Where-Object { $_ -match 'Microsoft\.EntityFrameworkCore' }

# Key variables
Write-Output "**Key Variables**:"
Write-Output "- ``DB_ENGINE`` = $(if ($detectedDb.Count -gt 0) { $detectedDb -join ', ' } else { 'NONE' })"
Write-Output "- ``DATA_ACCESS`` = $(if ($detectedOrm.Count -gt 0) { $detectedOrm -join ', ' } else { 'NONE' })"
Write-Output "- ``HAS_EFCORE`` = $(if ($hasEfCore) { 'true' } else { 'false' })"
Write-Output ""

