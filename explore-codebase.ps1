# 6. Connection Strings
# ═════════════════════════════════════════════
Write-Output "### Connection String Detection"
Write-Output ""
$configFiles = Get-ChildItem -Path $Root -Include "appsettings*.json","web.config","app.config" -Recurse -File |
    Where-Object { -not (Excluded $_.FullName) }

$connStringRows = @()
foreach ($cf in $configFiles) {
    $content = Get-Content $cf.FullName -Raw
    if ($content -match 'ConnectionString|connectionString|Data Source|Server=|Host=|Filename=') {
        $providers = @()
        if ($content -match 'Server=.*Database=|Data Source=.*Initial Catalog=|\.database\.windows\.net') { $providers += "SQL Server / Azure SQL" }
        if ($content -match 'Host=.*Port=.*Database=|postgresql|postgres') { $providers += "PostgreSQL" }
        if ($content -match 'server=.*port=3306|mysql') { $providers += "MySQL / MariaDB" }
        if ($content -match 'Data Source=.*\.db|Data Source=.*\.sqlite|Filename=.*\.db') { $providers += "SQLite" }
        if ($content -match 'User Id=.*Data Source=.*oracle|OracleConnection') { $providers += "Oracle" }
        if ($content -match 'snowflake|account=.*\.snowflakecomputing\.com') { $providers += "Snowflake" }
        $providerStr = if ($providers.Count -gt 0) { $providers -join ', ' } else { "_Unknown_" }
        $connStringRows += [PSCustomObject]@{ File = Rel $cf.FullName; Provider = $providerStr }
    }
}

if ($connStringRows.Count -gt 0) {
    Write-Output "| Config File | Likely Provider |"
    Write-Output "|---|---|"
    foreach ($r in $connStringRows) { Write-Output "| ``$($r.File)`` | $($r.Provider) |" }
    Write-Output ""
} else {
    Write-Output "_No connection strings detected._"
    Write-Output ""
}

