Write-Output '```'

function Get-Tree([string]$Path, [int]$Depth, [int]$MaxDepth, [string]$Prefix = '') {
    if ($Depth -ge $MaxDepth) { return }
    $items = Get-ChildItem -Path $Path |
        Where-Object { $_.Name -notmatch '^(bin|obj|node_modules|\.git|\.vs|packages|TestResults|\.idea)$' } |
        Sort-Object { -not $_.PSIsContainer }, Name

    for ($i = 0; $i -lt $items.Count; $i++) {
        $item = $items[$i]
        $isLast = ($i -eq $items.Count - 1)
        $connector = if ($isLast) { '└── ' } else { '├── ' }
        $childPrefix = if ($isLast) { '    ' } else { '│   ' }

        if ($item.PSIsContainer) {
            Write-Output "$Prefix$connector$($item.Name)/"
            Get-Tree $item.FullName ($Depth + 1) $MaxDepth "$Prefix$childPrefix"
        } else {
            Write-Output "$Prefix$connector$($item.Name)"
        }
    }
}

